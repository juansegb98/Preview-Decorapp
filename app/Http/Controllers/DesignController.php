     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create( Request $request)
    {
        $model = $request->design;
        $design = new Design;
        $design->art_id = $model["art"];
        $design->frame_id = $model["frame"];
        $design->liner_id = $model["liner"];
        $design->user_id = $model["user"];
        $design->background_id = 0;
        $design->observation = $model["observation"];
        $design->save();
        return $design;
    }

